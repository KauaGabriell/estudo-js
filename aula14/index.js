let Umastring = "Apenas um teste";
console.log("Deixar tudo maiúsculo.")
console.log(Umastring.toUpperCase());
console.log('----');

console.log("Deixar tudo minúsculo.")
console.log(Umastring.toLowerCase());
console.log('----');

console.log("Replace(Substituir)")
console.log(Umastring.replace('teste', 'show'));
console.log('----');

console.log("Slice(Cortar,fatiar)")
console.log(Umastring.slice(0, 6));
console.log(Umastring.slice(-5));
console.log('----');

console.log("Saber quantidade de caracteres");
console.log(Umastring.length);


